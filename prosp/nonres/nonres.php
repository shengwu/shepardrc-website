<h5>Why should I be a non-resident member of Shepard?</h5>
<p>Friends, food, and fun!</p>
<p>But seriously, by joining Shepard as a non-res, you will join a community of diverse, friendly students who you can meet through events like firesides, munchies, and midnight ultimate frisbee. You also get a key that lets you into the building.</p>

<h5>How much does it cost?</h5>
<p>Your dues are $40 for the whole year. That's less than residents pay per quarter.</p>
