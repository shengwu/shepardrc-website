<h1>Resources for new students</h1>
<h2>Student publications</h2>
<p>Student publications are a great way to learn about Northwestern's culture. The main ones are <a target="_blank" href="http://www.dailynorthwestern.com/">The Daily Northwestern</a>, a print newspaper, and <a target="_blank" href="http://northbynorthwestern.com/">North by Northwestern</a>, a more casual publication. More avant-garde news sources are <a target="_blank" href="http://sherman-ave.com/">Sherman Ave.</a>, a fresh, sarcastic blog about life and <a target="_blank" href="http://www.nuintel.net/">NU Intel</a>, a grungier take on student-related news. In addition to written news, we have broadcast news from <a target="_blank" href="http://www.nnntv.org/">Northwestern News Network</a> and a radio station called <a target="_blank" href="http://www.wnur.org/">WNUR 89.3 FM</a> that many students participate in.</p>

<h2>Getting involved</h2>
<p>Thanks to a recently launched initiative, <a target="_blank" href="http://northwestern.collegiatelink.net/">Wildcat Connection</a>, it's easy for newly accepted students to get interested in student organizations even before arriving on campus. Some particularly effective student organizations include <a target="_blank" href="http://www.nudm.org/">Dance Marathon</a>, a fundraising sprint followed by a 30-hour dance session that has raised over $1M each year for the last two years; <a target="_blank" href="http://www.ncdcnorthwestern.org/">NCDC</a>, an organization that facilitates student volunteering at many different Chicagoland nonprofits; and <a target="_blank" href="http://www.globemed.org/">GlobeMed</a>, a national organization founded at Northwestern that partners university students with grassroots organizations to improve global health. </p>

<p>You've probably seen emails about pre-orientation programs. We highly recommend that you participate in a pre-orientation program &mdash; it's well worth the money and all of them are excellent. This writer has a soft spot for <a target="_blank" href="http://groups.northwestern.edu/fup/">Freshman Urban Program</a>, though.</p>



