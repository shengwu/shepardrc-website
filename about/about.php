    <p style="padding-bottom: 2em;">The original building was made possible by a donation from Mrs. Margaret Bowen Shepard to honor her husband, Abraham D. Shepard, and her sister, Anna Maude Bowen, both of whom had been interested in education and young people. Mr. Shepard was secretary and general manager of the Pacific Improvement Co., which helped build the Union Pacific, Central Pacific, and Southern Pacific Railroads. Anna Bowen was the dean of women and an assistant professor of English at Northwestern from September 1899 until her sudden death in January of 1900.
