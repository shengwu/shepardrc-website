    <h2>History</h2>
    <p>Shepard Hall was constructed as part of Northwestern's Centennial celebration and dedicated in November 1952 as an addition to the women's quadrangles. The Holabird, Root, and Burgee design cost $780,000 to build. Originally housing 115 women, Shepard was expanded in 1960 with an addition for 65 students. In fall 1972 Shepard became a nonthematic, coeducational residential college with thematic clusters. Robert Church, associate dean for academic affairs and a professor of education, was the first master.</p>
