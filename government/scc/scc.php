<h5>What is SCC?</h5>
<p>The Shepard Community Council is a group of student volunteers that meets weekly to coordinate events for the Shepard community.</p>

<h5>How do I run for SCC?</h5>
<p>SCC elections are held at the beginning of each quarter at a designated Munchies. Most of the positions are quarterly, although some of them, like Historian, have three quarter terms. Learn more by reading the Constitution on our <a href="policies">Policies</a> page.</p>

<h5>Why should I run for SCC?</h5>
<p>Housing points, getting engaged with the community, and meeting lots of new people!</p>
