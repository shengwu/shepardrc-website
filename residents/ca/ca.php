<h5>What is a CA?</h5>
<p>A community assistant (CA) is a student responsible for keeping residents happy and safe. They organize social events and serve as liaisons between students and residence hall administration. If you have a complaint about the bathrooms or other facilities, they are the people to go to. In addition, there will be a CA on duty from 10 PM to 8:30 AM each night to answer the duty phone and respond to emergencies as needed.</p>

<p>It's a good idea to befriend your CA, because they're generally awesome people.</p>

<h5>What is an RHC?</h5>
<p>A residence hall coordinator (RHC) is a student who's sort of the head honcho of the CAs. They undertake administrative responsibities for the dorm.</p>

<p>For more information or to learn how to become a CA or an RHC, visit <a target="_blank" href="http://www.northwestern.edu/living/getting-involved/index.html">this page</a>.</p>

<h5>How do I contact my CA?</h5>
<p>If you need something urgently, call the Shepard / PARC CA duty phone at (847) 815-2320.</p>
<p>Otherwise, just email the CA that lives on your floor and they will follow up with you.</p>


